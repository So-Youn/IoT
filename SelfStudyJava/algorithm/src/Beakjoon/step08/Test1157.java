package Beakjoon.step08;
import java.util.*;
public class Test1157 {
//가장 많이 사용된 알파벳 찾기.
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		String input = sc.next();
		for (int i=0;i<input.length();i++) {
			System.out.println(input.charAt(i));
		}
		sc.close();
	}

}
